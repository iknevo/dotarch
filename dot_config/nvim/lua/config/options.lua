vim.opt.relativenumber = true
vim.opt.list = false

-- prevent commenting the next line to a comment
vim.api.nvim_create_autocmd("FileType", {
  pattern = "*",
  callback = function()
    vim.opt_local.formatoptions:remove({ "r", "o" })
  end,
})
